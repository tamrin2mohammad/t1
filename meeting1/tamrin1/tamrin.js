function sum(num1, num2) {
    return num1 + num2
}
function sub(num1, num2) {
    return num1 - num2
}
function mul(num1, num2) {
    return num1 * num2
}
function div(num1, num2) {
    return num1 / num2
}
console.log(sum(1,4))
console.log(sub(6,8))
console.log(mul(4,5))
console.log(div(20,4))

